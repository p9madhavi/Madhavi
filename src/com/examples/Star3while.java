package com.examples;


public class Star3while{

	public static void main(String[] args)
	{
		int i =1;
		
		while(i<=5){
           int j=1;
			while(j<=i)
			{
			System.out.print("*");
			j++;
			}
		
		System.out.println( );
		i++;}
		          
       
        	
		while(i<=4)
		{
           int j=4;
			while(j>=i)
			{
			System.out.print("*");
			j--;
			}
		
		System.out.println( );
		i++;
		
	   }  
		
}

	
}

