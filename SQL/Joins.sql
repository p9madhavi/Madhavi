SELECT W.LAST_NAME "EMPLOYEE",W.EMPLOYEE_ID "EMP#",
      M.LAST_NAME "MANAGER_NAME",M.MANAGER_ID "MGR#"
FROM EMPLOYEES W JOIN EMPLOYEES M
ON (W.MANAGER_ID = M.EMPLOYEE_ID)


