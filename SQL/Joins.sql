------ 3 -------
SELECT E.LAST_NAME,E.JOB_ID,E.DEPARTMENT_ID,
D.DEPARTMENT_NAME
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON (E.DEPARTMENT_ID = D.DEPARTMENT_ID)
JOIN LOCATIONS Loc
ON (D.LOCATION_ID = Loc.LOCATION_ID)
WHERE LOWER(Loc.CITY) = 'washington'

