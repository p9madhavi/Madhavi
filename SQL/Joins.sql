SELECT EMP.LAST_NAME "EMPLOYEE",EMP.EMPLOYEE_ID "EMP#",
        MGR.LAST_NAME "MANAGER_NAME",MGR.MANAGER_ID "MGR#"
FROM EMPLOYEES EMP LEFT OUTER JOIN EMPLOYEES MGR
ON (EMP.MANAGER_ID = MGR.EMPLOYEE_ID)
ORDER BY EMP.EMPLOYEE_ID


